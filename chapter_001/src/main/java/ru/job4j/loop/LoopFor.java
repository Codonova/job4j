package ru.job4j.loop;

/**
 * LoopFor
 *
 * @author Ruslan Kooslapchenkov
 */

public class LoopFor {

    /**
     * main
     *
     * @param args args
     */

    public static void main(String[] args) {
        for (int i = 5; i <= 10; i++) {
            System.out.println(i);
        }
    }
}
